package com.example.guojialin.worker.bean;

public class AiDiscern {
    private float leftX;
    private float topY;
    private float questionWidth;
    private float questionHeight;
    private boolean ansResult;

    public float getLeftX() {
        return leftX;
    }

    public void setLeftX(float leftX) {
        this.leftX = leftX;
    }

    public float getTopY() {
        return topY;
    }

    public void setTopY(float topY) {
        this.topY = topY;
    }

    public float getQuestionWidth() {
        return questionWidth;
    }

    public void setQuestionWidth(float questionWidth) {
        this.questionWidth = questionWidth;
    }

    public float getQuestionHeight() {
        return questionHeight;
    }

    public void setQuestionHeight(float questionHeight) {
        this.questionHeight = questionHeight;
    }

    public boolean isAnsResult() {
        return ansResult;
    }

    public void setAnsResult(boolean ansResult) {
        this.ansResult = ansResult;
    }
}
